/**
 * Get quotes for a specific book
 */
export function getBookQuotes(bookTitle) {
  return BOOK_QUOTES[bookTitle] || [];
}

/**
 * Get a random quote from a book
 */
export function getRandomQuote(bookTitle) {
  const quotes = getBookQuotes(bookTitle);
  if (quotes.length === 0) return null;
  return quotes[Math.floor(Math.random() * quotes.length)];
}
